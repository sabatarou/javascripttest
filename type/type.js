//数字を代入
var answer=42;
document.write(typeof answer+"<br>");
//文字列を代入
var answer="しにたい";
document.write(typeof answer+"<br>");
//数値と文字列を足すと文字列
x="答え"+42;
y=42+"が答え";
document.write(x+"<br>"+typeof x+"<br>");
document.write(y+"<br>"+typeof y+"<br>");
//文字列から数値への変換
document.write(("1.1"+"1.1")+typeof ("1.1"+"1.1"));
